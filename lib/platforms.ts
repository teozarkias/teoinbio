export function getPlatform(id: string): Platform | undefined {
  return platforms.find((p) => p.id === id);
}

export function buildUrl(platformId: string, username: string): string {
  const platform = getPlatform(platformId);
  if (!platform) return username;
  if (platformId === "custom") return username;
  if (platformId === "linkedin") {
    const match = username.match(/linkedin\.com\/in\/([^/?]+)/);
    return match
      ? `https://linkedin.com/in/${match[1]}`
      : `https://linkedin.com/in/${username}`;
  }
  if (platformId === "facebook") {
    if (username.startsWith("http")) return username;
    return `https://facebook.com/${username}`;
  }
  return `${platform.baseUrl}${username}`;
}
